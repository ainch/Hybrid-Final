// Cross Section Fitting Function
float FUNC(float *m_x, float *m_y, int m_size, float in_x)
// Original function used GPU_origin
{
	if(m_size > 1) {
		if(in_x < m_x[0]) {
			return m_y[0];
		}
		else if(in_x > m_x[m_size-1]) {
			return m_y[m_size-1];
		}

		int k;
		int k_low=0;
		int k_hi=m_size-1;
		float h, b, a;

		while(k_hi-k_low > 1) {
			k = (k_hi + k_low) >> 1;
			if(m_x[k] > in_x) k_hi = k;
			else k_low = k;
		}

		h = m_x[k_hi]-m_x[k_low];
		if(h == 0.0) {
			printf("BAD CROSS SECTION DATA.\n");
		}

		a = (m_x[k_hi] - in_x)/h;
		b = (in_x - m_x[k_low])/h;

		return (a*m_y[k_low] + b*m_y[k_hi]);
	}
	return m_y[0];
}
float FUNC_decay(float *m_x, float *m_y, int m_size, float in_x)
// update more physically than FUNC
{
	if(m_size > 1) {
		if(in_x < m_x[0]) {
			return m_y[0];
		}
		else if(in_x > m_x[m_size-1]) {
			float inc = log10(m_y[m_size-2]/m_y[m_size-1])/log10(m_x[m_size-1]/m_x[m_size-2]);
			return m_y[m_size-1] * pow(m_x[m_size-1],inc) / pow(in_x,inc);
		}
		int k;
		int k_low=0;
		int k_hi=m_size-1;
		float h, b, a;

		while(k_hi-k_low > 1) {
			k = (k_hi + k_low) >> 1;
			if(m_x[k] > in_x) k_hi = k;
			else k_low = k;
		}

		h = m_x[k_hi]-m_x[k_low];
		if(h == 0.0) {
			printf("BAD CROSS SECTION DATA.\n");
		}

		a = (m_x[k_hi] - in_x)/h;
		b = (in_x - m_x[k_low])/h;

		return (a*m_y[k_low] + b*m_y[k_hi]);
	}
	return m_y[0];
}
float FUNC_LE(float engy, float dLE, float *CX_x, float *CX_y, int CX_size){
// update more physically than FUNC (log scale Energy + exponential decay)
	float LogE = 0;
	float temp = 0;
	float eng;
	int E1;
	float E0,E2,E3;
	//dLE = idLOGX // dlogE and scale
	if(engy == 0) return 0.0;
	LogE = log10(engy);

	if (LogE <= CX_x[0]){
		temp = CX_y[0];
		return temp;
	}else if(LogE >= CX_x[CX_size-1]){ // exponential decay
		temp = CX_y[CX_size-1] * 0.1 * exp(-1 * (LogE - CX_x[CX_size-1]));
		return temp;
	}
	eng = LogE - CX_x[0];
	E0 = eng * dLE;
	E1 = (int) E0;
	E2 = E0 - E1;
	E3 = 1 - E2;
	//fprintf (stderr,"LogE=%g,eng=%g,E0=%g,E1=%d,E2=%g,E3=%g ",LogE,eng,E0,E1,E2,E3);
	temp = E3 * CX_y[E1] + E2 * CX_y[E1+1];
	return temp;
}
float Log_FUNC(float idx, float *m_x, float *m_y, int m_size, float in_x){
	// update more physically than FUNC (log scale decay)
	if(in_x == 0)return 0.0;
	float engy = log10(in_x);
	float ee1, a1, a2;
	int ee2;
	if(engy < m_x[0]){
		return m_y[0];
	}else if(engy > m_x[m_size-1]){
		return m_y[m_size-1] * 0.1 * exp(-1 * (engy - m_x[m_size-1]));
	}
	engy = engy - m_x[0];
	ee1 = idx * engy;
	ee2 = ee1;
	a1 = ee1 - ee2;
	a2 = 1 - a1;
	//fprintf(stderr,"%g %g %d %g %g\n",engy,ee1,ee2,a1,a2);
	return a2*m_y[ee2]+a1*m_y[ee2+1];
}
